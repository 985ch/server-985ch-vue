<template>
  <div class="login-container">
    <el-form ref="loginForm" :model="loginForm" :rules="loginRules" class="login-form" auto-complete="on" label-position="left">

      <div class="title-container">
        <h3 class="title">登录系统</h3>
      </div>

      <el-form-item prop="qq">
        <span class="svg-container">
          <svg-icon icon-class="user" />
        </span>
        <el-input
          ref="qq"
          v-model="loginForm.qq"
          placeholder="请输入QQ号"
          name="qq"
          type="text"
          tabindex="1"
          auto-complete="on"
        />
      </el-form-item>

      <el-form-item prop="code">
        <span class="svg-container">
          <svg-icon icon-class="password" />
        </span>
        <el-input
          ref="code"
          v-model="loginForm.code"
          placeholder="请输入登陆码"
          name="code"
          tabindex="2"
          auto-complete="on"
          @keyup.enter.native="handleLogin"
        />
      </el-form-item>

      <el-button :loading="loading" type="primary" style="width:100%;margin-bottom:30px;" @click.native.prevent="handleLogin">登录</el-button>

    </el-form>
  </div>
</template>

<script>
export default {
  name: 'Login',
  data() {
    const validateQQ = (rule, value, callback) => {
      if (value.trim().length < 5) {
        callback(new Error('必须输入一个有效的QQ号'))
      } else {
        callback()
      }
    }
    const validateCode = (rule, value, callback) => {
      if (!/\d\d\d\d\d\d/.test(value)) {
        callback(new Error('编码必须是6位数字'))
      } else {
        callback()
      }
    }
    return {
      loginForm: {
        qq: 'test',
        code: ''
      },
      loginRules: {
        qq: [{ required: true, trigger: 'blur', validator: validateQQ }],
        code: [{ required: true, trigger: 'blur', validator: validateCode }]
      },
      loading: false,
      redirect: undefined,
      otherQuery: {}
    }
  },
  watch: {
    $route: {
      handler: function(route) {
        const query = route.query
        if (query) {
          this.redirect = query.redirect
          this.otherQuery = this.getOtherQuery(query)
        }
      },
      immediate: true
    }
  },
  async beforeMount() {
    const qq = this.$route.query.qq
    const code = this.$route.query.code
    if (qq && code) {
      this.loading = true
      await this.$store.dispatch('user/login', { qq, code })
      this.$router.push({ path: this.redirect || '/', query: this.otherQuery })
      this.loading = false
    }
  },
  methods: {
    getOtherQuery(query) {
      return Object.keys(query).reduce((acc, cur) => {
        if (cur !== 'redirect') {
          acc[cur] = query[cur]
        }
        return acc
      }, {})
    },
    handleLogin() {
      this.$refs.loginForm.validate(valid => {
        if (valid) {
          this.loading = true
          this.$store.dispatch('user/login', this.loginForm).then(() => {
            this.$router.push({ path: this.redirect || '/', query: this.otherQuery })
            this.loading = false
          }).catch(() => {
            this.loading = false
          })
        } else {
          console.log('error submit!!')
          return false
        }
      })
    }
  }
}
</script>

